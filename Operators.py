
# Arithmetic Operators
num1= 10
num2= 20
print("Num1 + Num2 =",num1+num2)
print("Num1 - Num2 =",num1-num2)
print("Num1 * Num2 =",num1*num2)
print("Num1 / Num2 =",num1/num2)
print('5^3 =',5**3)
print('20%3 =',20%3)
print('22//7=',22//7)

# Assignment Operators
num3 = num1+num2
num3 +=num2  #num3 = num3+num2
print(num3)

# Comparison Operators
print("Is num3 > num2?",num3>num2)
print("Is num2 = num3?",num2==num3)
print("Is num1 != num2?",num1!=num2)

# Logical Operators
x = True
y = False
print("X and Y",x and y)
print("X or Y",x or y)
print("not of x",not x)


# Bitwise Operators
num4 = 6 #110
num5 = 2 #010
print('Bitwise and =',num4 & num5)
print('Bitwise or =',num4 | num5)
print('Bitwise xor =',num4 ^ num5)

print("Num 4 rightshift by 2",num4 >> 2)
print("Num 4 Leftshift by 2",num4 << 2)








